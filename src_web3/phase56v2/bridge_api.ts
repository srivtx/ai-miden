        bridgeState, // WHY: State for threshold and bumps.
        burnRecord, // WHY: Record to prevent double release.
        vault, // WHY: Source of original tokens.
        recipientTokenAccount, // WHY: Destination for original tokens.
        guardianRegistry, // WHY: Registry for signature verification.
        tokenProgram: TOKEN_PROGRAM_ID, // WHY: SPL Token program for transfer CPI.
