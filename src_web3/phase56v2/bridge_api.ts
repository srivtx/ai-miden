app.get("/bridge/status/:tx", async (req, res) => { // WHY: Register an endpoint to inspect a transaction or lock/burn record by nonce.
  try { // WHY: Handle lookup errors.
    const { tx } = req.params; // WHY: Extract the transaction signature or nonce identifier from the URL.
    const txInfo = await connection.getTransaction(tx, { commitment: "confirmed" }); // WHY: Attempt to fetch the full transaction metadata from the cluster.
    if (txInfo) { // WHY: If the cluster returns data, respond with transaction details.
      return res.json({ type: "transaction", info: txInfo }); // WHY: Return transaction info and exit to avoid further lookup.
    } // WHY: End signature lookup branch.
    const nonce = parseInt(tx, 10); // WHY: Parse the parameter as an integer nonce.
    const lockRecordSeed = new BN(nonce).toArrayLike(Buffer, "le", 8); // WHY: Convert the nonce to seed bytes.
    const lockRecord = PublicKey.findProgramAddressSync([Buffer.from("lock_record"), lockRecordSeed], bridgeProgramId)[0]; // WHY: Derive the lock record for this nonce.
    try { // WHY: Attempt to fetch the lock record.
      const lockData = await bridgeProgram.account.lockRecord.fetch(lockRecord); // WHY: Fetch the lock record from chain.
      return res.json({ type: "lock", data: lockData }); // WHY: Return lock details if found.
    } catch (_) { // WHY: Ignore lock fetch failure and continue to burn lookup.
    } // WHY: End lock lookup.
    const burnRecord = PublicKey.findProgramAddressSync([Buffer.from("burn_record"), lockRecordSeed], bridgeProgramId)[0]; // WHY: Derive the burn record for this nonce.
    try { // WHY: Attempt to fetch the burn record.
      const burnData = await bridgeProgram.account.burnRecord.fetch(burnRecord); // WHY: Fetch the burn record from chain.
      return res.json({ type: "burn", data: burnData }); // WHY: Return burn details if found.
    } catch (_) { // WHY: Ignore burn fetch failure.
    } // WHY: End burn lookup.
    res.status(404).json({ error: "Not found" }); // WHY: Return 404 if nothing matches the identifier.
  } catch (err) { // WHY: Catch unexpected lookup errors.
    res.status(500).json({ error: String(err) }); // WHY: Return error.
  } // WHY: Close the try block.
}); // WHY: Close the /bridge/status route handler.

