// WHY: GET /rewards/:user - Calculate pending rewards for a user without claiming
app.get("/rewards/:user", async (req, res) => { // WHY: Async for blockchain read
    try { // WHY: Error handling
        const { user } = req.params; // WHY: Extract user from URL path
        const { pool } = req.query; // WHY: Extract pool from query string

        if (!pool) { // WHY: Pool required for calculation
            return res.status(400).json({ error: "Pool query parameter required" }); // WHY: Bad request
        } // WHY: Prevent errors

        const userPubkey = new PublicKey(user); // WHY: Convert to PublicKey
        const poolPubkey = new PublicKey(pool as string); // WHY: Convert query string to PublicKey

        const [userInfoPDA] = deriveUserInfoPDA(poolPubkey, userPubkey); // WHY: Derive user info

        // WHY: Fetch user info and pool state from chain
        const userInfo = await program.account.userInfo.fetch(userInfoPDA); // WHY: On-chain user state
        const poolAccount = await program.account.pool.fetch(poolPubkey); // WHY: On-chain pool state

        // WHY: Calculate pending rewards using same math as on-chain program
        const staked = new BN(userInfo.stakedAmount); // WHY: User's stake as BN
        const rps = new BN(poolAccount.rewardPerShare.toString()); // WHY: Global accumulator as BN
        const debt = new BN(userInfo.rewardDebt.toString()); // WHY: User's snapshot as BN

        // WHY: pending = (staked * rps) / PRECISION - debt / PRECISION
        const PRECISION_BN = new BN("1000000000000000000"); // WHY: 1e18 precision factor
        const accrued = staked.mul(rps); // WHY: staked * reward_per_share
        const pendingRaw = accrued.sub(debt); // WHY: Subtract already-paid rewards
        const pending = pendingRaw.div(PRECISION_BN); // WHY: Remove precision factor

        return res.json({ // WHY: Return calculation results
            success: true, // WHY: Request succeeded
            user: userPubkey.toBase58(), // WHY: Human-readable user address
            pool: poolPubkey.toBase58(), // WHY: Human-readable pool address
            stakedAmount: userInfo.stakedAmount.toString(), // WHY: User's current stake as string
            rewardPerShare: poolAccount.rewardPerShare.toString(), // WHY: Global accumulator
            rewardDebt: userInfo.rewardDebt.toString(), // WHY: User's snapshot
            pendingRewards: pending.toString(), // WHY: Calculated pending rewards
        }); // WHY: Frontend displays this without signing transaction
    } catch (error) { // WHY: Error handling
        console.error("Rewards query error:", error); // WHY: Server log
        return res.status(500).json({ error: String(error) }); // WHY: Server error
    } // WHY: Always respond
}); // WHY: Route complete

